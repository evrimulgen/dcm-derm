private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
    String directory = ""; 
    JFileChooser fileChooser = new JFileChooser(directory);
    fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
    fileChooser.setMultiSelectionEnabled(true);
    
    File[] selectedFiles = null;
    if (fileChooser.showOpenDialog(this) != 0 || (selectedFiles = fileChooser.getSelectedFiles()) == null) {
         inFiles = null;
         return;
    } else {
        inFiles = selectedFiles;
        if (inFiles.length == 1) {
            openField.setText(inFiles[0].getPath());
        }
        else {
            openField.setText(Messages.getString("ImgImportDialog.multiseletion"));
        }
    }
}//GEN-LAST:event_openButtonActionPerformed

private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
    String directory = ""; 
    JFileChooser fileChooser = new JFileChooser(directory);
    fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
    fileChooser.setMultiSelectionEnabled(false);
     
    File selectedFile = null;
    if (fileChooser.showSaveDialog(this) != 0 || (selectedFile = fileChooser.getSelectedFile()) == null) {
        outFile = null;
        return;
    } else {
        outFile = selectedFile;
        saveField.setText(outFile.getPath());
    }
}//GEN-LAST:event_saveButtonActionPerformed
    
